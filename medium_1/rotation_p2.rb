# option 2
def rotate_rightmost_digits(int, k)
  return int if k == 1
  str = int.to_s
  (str[0..(-k -1)] + str[(-k +1)..-1] + str[-k]).to_i
end

# LS way
def rotate_rightmost_digits(number, n)
  all_digits = number.to_s.chars
  all_digits[-n..-1] = rotate_array(all_digits[-n..-1])
  all_digits.join.to_i
end
